function [vA] = Alfven_velocity(B,m,n)
%ALFVEN_VELOCITY Summary of this function goes here
%   Detailed explanation goes here
    mu0     = 4*pi*1e-7;        %vacuum permeability
    B_mag   = sqrt(sum(B.^2,2));
    vA      = B_mag./sqrt(mu0*(sum(m.*n,2)));
end

